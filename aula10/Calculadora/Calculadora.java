package aula10.Calculadora;

public class Calculadora {
	
	public Calculadora() {}
	
	public int soma(int a, int b) {
		System.out.println("Somando 2 inteiros");
		return a+b;
	}
	public float soma(float a, float b) {
		System.out.println("Somando 2 floats");
		return a+b;		
	}
	public double soma(double a, double b) {
		System.out.println("Somando 2 doubles");
		return a+b;
	}
	public int soma(int a,int b, int c) {
		System.out.println("Somando 3 inteiros");
		return a+b+c;		
	}
	public float soma(float a, float b, float c) {
		System.out.println("Somando 3 floats");
		return a+b+c;
	}
	public double soma(double a, double b, double c) {
		System.out.println("Somando 3 doubles");
		return a+b+c;
	}
}












